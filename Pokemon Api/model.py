class Pokemon:

    def __init__(self, id, name, height, weight, xp, image_url, pokemon_url, abilities, stats, types):
        self.id = id
        self.name = name
        self.height = height
        self.weight = weight
        self.xp = xp
        self.image_url = image_url
        self.pokemon_url = pokemon_url
        self.abilities = abilities
        self.stats = stats
        self.types = types